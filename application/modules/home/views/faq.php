<h3>
	<b>1. What is Reputation points?</b>
</h3>
<p>
	Reputation points score is a rough measurement of how much the community trusts you; it is earned by convincing your peers that you know what you’re talking about.
</p>
<h3>
	<b>
		2. How do I earn the Reputation points?
	</b>
</h3>
<p>
The primary way to gain reputation points is by posting good questions and useful answers. Votes on these posts cause you to gain (or sometimes lose) reputation. Also accepting the answer to your question will increase your reputation points.
</p>
<b>You gain reputation points  when:</b>
<ul>
    <li>Your question is voted up: +10</li>
    <li>Your answer is voted up:  +10</li>
    <li>Your answer is marked “accepted”: +15 (+2 to question creator)</li>
    <li>You will not gain any reputation points  if you accept your answer for your own question</li>
</ul>
 
<b>
You lose the reputation points  when:
</b>
	<ul>
    <li>Your question is voted down:  −2</li>
    <li>Your answer is voted down:  −2</li>
    <li>You vote down an answer/question:  −1</li>
</ul>
<h3>
	<b>3. How many reputation points do I need for different actions?</b>
</h3>
<p>
    To upload a question/answer you do not require any reputation points 
    To vote up question/answer you require a minimum of 15 reputation points 
    To vote down question/answer you require a minimum of 30 reputation points 
    An admin of a particular course can do all the actions without any reputation points.
</p>
<p>
	If you are the creator, you do not require any reputation points  to do any actions on that particular question/answer other than upvote/down(you can not up/down vote your own question/answer)
    To edit the question/answer created by another user, you require 500 reputation points
    To delete the question/answer created by  another user, you require 1000 reputation points
</p>			
